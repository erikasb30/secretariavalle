SELECT       
T103DAFINAPROC.A103LLAVPROC AS NUMERO_PROCESO, 
T103DAFINAPROC.A103FECHFINA AS FECHA_FINALIZACIÓN, 
T053BACLASGENE.A053DESCCLAS AS CLASE_PROCESO, 
T103DAINFOPROC.A103FLAGVIGE AS VIGENTE, 
T061BAUBICGENE.A061DESCUBIC AS UBICACION, 
T103DAINFOPROC.A103NOMBPONE AS MAGISTRADO_PONENTE, 
T103DAINFOPROC.A103FECHREPA AS FECHA_REPARTO, 
T112DRSUJEPROC.A112CODISUJE AS DEMANTANTE, 
T112DRSUJEPROC.A112NOMBSUJE AS NOMBRE_DEMANTANTE, 
T112DRSUJEPROC_1.A112CODISUJE AS DISCIPLINADO, 
T112DRSUJEPROC_1.A112NOMBSUJE AS NOMBRE_DISCIPLINADO,
T103DAADICPROC.A103CONTRADI
FROM            
T103DAFINAPROC INNER JOIN
T103DAINFOPROC ON T103DAFINAPROC.A103LLAVPROC = T103DAINFOPROC.A103LLAVPROC INNER JOIN
T053BACLASGENE ON T103DAINFOPROC.A103CODICLAS = T053BACLASGENE.A053CODICLAS INNER JOIN
T061BAUBICGENE ON T103DAINFOPROC.A103CODIUBIC = T061BAUBICGENE.A061CODIUBIC INNER JOIN
T112DRSUJEPROC ON T103DAINFOPROC.A103LLAVPROC = T112DRSUJEPROC.A112LLAVPROC INNER JOIN
T103DAADICPROC ON T103DAINFOPROC.A103LLAVPROC = T103DAADICPROC.A103LLAVPROC
WHERE        
(T103DAFINAPROC.A103FECHFINA IS NULL) AND 
(T103DAINFOPROC.A103FLAGVIGE = 'si') AND 
(T112DRSUJEPROC.A112CODISUJE = '0001') AND 
(T112DRSUJEPROC_1.A112CODISUJE = '0002')